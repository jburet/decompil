package model.attribute;

public class ElementValuePair {
	private short indexConstant;
	private ElementValue elementValue;

	public short getIndexConstant() {
		return indexConstant;
	}

	public void setIndexConstant(short indexConstant) {
		this.indexConstant = indexConstant;
	}

	public ElementValue getElementValue() {
		return elementValue;
	}

	public void setElementValue(ElementValue elementValue) {
		this.elementValue = elementValue;
	}
}
